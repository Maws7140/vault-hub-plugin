    const imageMatch = value.match(/^!\[[^\]]*\]\(([^)\s]+)(?:\s+["'][^"']*["'])?\)$/);
    if (imageMatch)
      return imageMatch[1];
    const linkMatch = value.match(/^\[[^\]]+\]\(([^)\s]+)(?:\s+["'][^"']*["'])?\)$/);
    if (linkMatch)
      return linkMatch[1];
    return value.replace(/^["']|["']$/g, "");
